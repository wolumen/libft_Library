#include "libft.h"

int	ft_isspace(int c)
{
	char	x;

	x = (char) c;
	if (x == ' ' || x == '\f' || x == '\n' || x == '\r' || x == '\t'
		|| x == '\v')
		return (1);
	return (0);
}
